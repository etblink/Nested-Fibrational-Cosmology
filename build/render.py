from __future__ import annotations

import json
import re
from pathlib import Path
from typing import Any, Dict, List

import yaml

ROOT = Path(__file__).resolve().parents[1]
CLAIMS_DIR = ROOT / "claims"
CANON_DIR = ROOT / "canon"
REPORTS_DIR = ROOT / "output" / "reports"
TABLES_DIR = ROOT / "output" / "tables"
TEX_DIR = ROOT / "output" / "tex"
TEX_DIR.mkdir(parents=True, exist_ok=True)


def load_yaml(path: Path) -> Dict[str, Any]:
    with path.open("r", encoding="utf-8") as f:
        return yaml.safe_load(f) or {}


def load_json(path: Path) -> Dict[str, Any]:
    with path.open("r", encoding="utf-8") as f:
        return json.load(f)


def load_claims() -> Dict[str, Dict[str, Any]]:
    claims: Dict[str, Dict[str, Any]] = {}
    for path in sorted(CLAIMS_DIR.rglob("*.yaml")):
        data = load_yaml(path)
        cid = data.get("id")
        if cid:
            data["_path"] = str(path.relative_to(ROOT))
            claims[cid] = data
    return claims


def load_release_postures() -> Dict[str, str]:
    release = load_yaml(CANON_DIR / "releases" / "L_plus_4.yaml")
    return release.get("effective_branch_postures", {})


def tex_escape(value: Any) -> str:
    s = "" if value is None else str(value)
    # Keep IDs readable while escaping TeX-sensitive characters.
    replacements = {
        "\\": r"\textbackslash{}",
        "&": r"\&",
        "%": r"\%",
        "$": r"\$",
        "#": r"\#",
        "_": r"\_",
        "{": r"\{",
        "}": r"\}",
        "~": r"\textasciitilde{}",
        "^": r"\textasciicircum{}",
    }
    return "".join(replacements.get(ch, ch) for ch in s)


def write_table_tex(path: Path, section: str, headers: List[str], rows: List[List[Any]]) -> None:
    colspec = "l" * len(headers)
    lines = [
        "% Auto-generated by build/render.py. Do not edit by hand.",
        rf"\section*{{{tex_escape(section)}}}",
        "",
        rf"\begin{{tabular}}{{{colspec}}}",
        " & ".join(rf"\textbf{{{tex_escape(h)}}}" for h in headers) + r"\\",
        r"\hline",
    ]
    for row in rows:
        lines.append(" & ".join(tex_escape(cell) for cell in row) + r"\\")
    lines.append(r"\end{tabular}")
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> None:
    claims = load_claims()
    release_postures = load_release_postures()
    computed = load_json(REPORTS_DIR / "computed_statuses.json")
    audit = load_json(REPORTS_DIR / "audit_report.json")

    # Branch posture table.
    posture_rows: List[List[Any]] = []
    release_rows: List[List[Any]] = []
    for cid, claim in sorted(claims.items()):
        if claim.get("kind") == "status_note" and cid.startswith("status:"):
            branch = cid.split(":", 1)[1]
            declared = claim.get("declared_status", "")
            effective = computed.get(cid, {}).get("effective_status", "")
            release = release_postures.get(branch, "")
            source = computed.get(cid, {}).get("source", "")
            posture_rows.append([branch, declared, effective, release, source])
            if release and effective != release:
                release_rows.append([branch, effective, release, "computed-vs-release mismatch"])

    write_table_tex(
        TEX_DIR / "branch_postures.tex",
        "Branch Posture Reconciliation",
        ["Branch", "Declared", "Computed", "Release", "Source"],
        posture_rows,
    )

    # Active frontier / obligation table.
    frontier_rows: List[List[Any]] = []
    for cid, claim in sorted(claims.items()):
        eff = computed.get(cid, {}).get("effective_status", claim.get("declared_status"))
        if claim.get("kind") in {"obligation", "frontier"} and eff == "O":
            frontier_rows.append([
                cid,
                claim.get("book", ""),
                claim.get("title", ""),
                claim.get("scope", {}).get("certificate_scope", ""),
            ])
    write_table_tex(
        TEX_DIR / "active_frontiers.tex",
        "Active Frontiers and Open Obligations",
        ["ID", "Book", "Title", "Scope"],
        frontier_rows,
    )

    # Claim catalog.
    catalog_rows: List[List[Any]] = []
    for cid, claim in sorted(claims.items()):
        catalog_rows.append([
            cid,
            claim.get("kind", ""),
            claim.get("book", ""),
            claim.get("declared_status", ""),
            computed.get(cid, {}).get("effective_status", claim.get("declared_status", "")),
        ])
    write_table_tex(
        TEX_DIR / "claim_catalog.tex",
        "Claim Catalog",
        ["ID", "Kind", "Book", "Declared", "Effective"],
        catalog_rows,
    )

    # Audit issues table.
    issue_rows = []
    for issue in audit.get("issues", []):
        issue_rows.append([
            issue.get("severity", ""),
            issue.get("type", ""),
            issue.get("id", issue.get("branch", "")),
            issue.get("reason", issue.get("alias", "")),
        ])
    write_table_tex(
        TEX_DIR / "audit_issues.tex",
        "Governance Audit Issues",
        ["Severity", "Type", "ID/Branch", "Reason"],
        issue_rows,
    )

    # A single TeX include file for canon appendices.
    include_lines = [
        "% Auto-generated governance packet. Include from a LaTeX appendix if desired.",
        r"\input{branch_postures.tex}",
        r"\input{active_frontiers.tex}",
        r"\input{claim_catalog.tex}",
        r"\input{audit_issues.tex}",
    ]
    (TEX_DIR / "governance_packet.tex").write_text("\n".join(include_lines) + "\n", encoding="utf-8")

    # Release reconciliation markdown.
    md = [
        "# Release Reconciliation",
        "",
        "| Branch | Computed | Release | Result |",
        "|---|---|---|---|",
    ]
    for branch, release in sorted(release_postures.items()):
        cid = f"status:{branch}"
        computed_value = computed.get(cid, {}).get("effective_status", "missing")
        result = "match" if computed_value == release else "mismatch"
        md.append(f"| {branch} | {computed_value} | {release} | {result} |")
    (TABLES_DIR / "release_reconciliation.md").write_text("\n".join(md) + "\n", encoding="utf-8")

    print(json.dumps({
        "tex_files": 5,
        "branch_rows": len(posture_rows),
        "frontier_rows": len(frontier_rows),
        "claim_rows": len(catalog_rows),
        "issue_rows": len(issue_rows),
        "release_mismatches": len(release_rows),
    }, indent=2))


if __name__ == "__main__":
    main()
